// rawMessage wraps an iCalendar body in a minimal multipart/mixed RFC 822 iMIP
// message so Parse can find the text/calendar part. Used for hand-built invites
// the engine's generators would reject.
func rawMessage(ics string) []byte {
	const boundary = "b0undary"
	return []byte("From: olivia@example.com\r\n" +
		"To: andy@example.com\r\n" +
		"Subject: Invite\r\n" +
		"MIME-Version: 1.0\r\n" +
		"Content-Type: multipart/mixed; boundary=\"" + boundary + "\"\r\n" +
		"\r\n" +
		"--" + boundary + "\r\n" +
		"Content-Type: text/calendar; method=REQUEST; charset=utf-8\r\n\r\n" +
		ics +
		"\r\n--" + boundary + "--\r\n")
}

