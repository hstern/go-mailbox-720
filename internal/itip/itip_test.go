	if err != nil {
		t.Fatalf("ProcessRequest: %v", err)
	}

	if w.createCalls != 1 {
		t.Fatalf("CreateEvent calls = %d, want 1", w.createCalls)
	}
	if w.gotCalendar != "cal-1" {
		t.Errorf("calendarID = %q, want cal-1", w.gotCalendar)
	}
