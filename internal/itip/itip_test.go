// sampleEvent builds a stored calendar.Event of the shape the future
// accept/decline endpoints would load: an organizer to reply to and a couple of
// attendees, with no original REQUEST mail retained.
func sampleEvent() calendar.Event {
	var ev calendar.Event
	ev.UID = "evt-123@example.com"
	ev.Title = "Project sync"
	ev.SetUTCTimes(
		time.Date(2026, 6, 15, 15, 0, 0, 0, time.UTC),
		time.Date(2026, 6, 15, 16, 0, 0, 0, time.UTC),
	)
	org := calendar.NewParticipant("Olivia Organizer", "org@example.com", "", "owner")
	ev.SetOrganizerAttendees(&org, []jscalendar.Participant{
		calendar.NewParticipant("Andy Attendee", "andy@example.com", "", "attendee"),
		calendar.NewParticipant("Bea Attendee", "bea@example.com", "", "attendee"),
	})
	return ev
}

func TestInviteFromEvent(t *testing.T) {
	ev := sampleEvent()
	ev.Sequence = 5

	got := InviteFromEvent(ev)

	if got.Method != scheduling.MethodRequest {
		t.Errorf("Method = %q, want REQUEST", got.Method)
	}
	if got.UID != ev.UID {
		t.Errorf("UID = %q, want %q", got.UID, ev.UID)
	}
	if got.Summary != ev.Title {
		t.Errorf("Summary = %q, want %q", got.Summary, ev.Title)
	}
	if !got.Start.Equal(ev.StartTime()) {
		t.Errorf("Start = %v, want %v", got.Start, ev.StartTime())
	}
	if !got.End.Equal(ev.EndTime()) {
		t.Errorf("End = %v, want %v", got.End, ev.EndTime())
	}
	wantOrg := scheduling.Address{Name: "Olivia Organizer", Email: "org@example.com"}
	if got.Organizer != wantOrg {
		t.Errorf("Organizer = %+v, want %+v", got.Organizer, wantOrg)
	}
	if len(got.Attendees) != 2 {
		t.Fatalf("Attendees len = %d, want 2", len(got.Attendees))
	}
	for i, want := range []scheduling.Attendee{
		{Address: scheduling.Address{Name: "Andy Attendee", Email: "andy@example.com"}},
		{Address: scheduling.Address{Name: "Bea Attendee", Email: "bea@example.com"}},
	} {
		if got.Attendees[i] != want {
			t.Errorf("Attendees[%d] = %+v, want %+v (empty PARTSTAT)", i, got.Attendees[i], want)
		}
	}
	if got.Sequence != 5 {
		t.Errorf("Sequence = %d, want 5 (carried from the event)", got.Sequence)
	}
	// A master event (no RecurrenceID) yields an invite addressing the whole series.
	if got.RecurrenceID != "" {
		t.Errorf("RecurrenceID = %q, want empty for a master event", got.RecurrenceID)
	}
}

// InviteFromEvent threads a non-zero RecurrenceID through to the invite so the
// REPLY/REQUEST/CANCEL it backs targets a single occurrence of a series.
func TestInviteFromEventRecurrenceID(t *testing.T) {
	ev := sampleEvent()
	rid := localDateTimeUTC(time.Date(2026, 6, 22, 9, 0, 0, 0, time.UTC))
	ev.RecurrenceID = &rid
	ev.RecurrenceIDTimeZone = "Etc/UTC"

	got := InviteFromEvent(ev)

	if got.RecurrenceID != "20260622T090000Z" {
		t.Errorf("RecurrenceID = %q, want 20260622T090000Z", got.RecurrenceID)
	}
	// The reply echoes the RECURRENCE-ID, so the composed REPLY carries it.
	reply, err := scheduling.Reply(got, scheduling.Address{Email: "a@example.com"}, scheduling.PartStatAccepted)
	if err != nil {
		t.Fatalf("Reply: %v", err)
	}
	if !strings.Contains(string(reply), "RECURRENCE-ID") {
		t.Errorf("composed REPLY missing RECURRENCE-ID:\n%s", reply)
	}
}

// EventFromInvite parses a RECURRENCE-ID off an inbound invite into the neutral
// event's RecurrenceID, marking it an override.
func TestEventFromInviteRecurrenceID(t *testing.T) {
	inv := &scheduling.Invite{
		Method:  scheduling.MethodRequest,
		UID:     "series@example.com",
		Summary: "Standup",
		Start:   time.Date(2026, 6, 22, 10, 30, 0, 0, time.UTC),
	}
	inv.SetRecurrenceID(time.Date(2026, 6, 22, 9, 0, 0, 0, time.UTC))

	ev := EventFromInvite(inv)

	want := time.Date(2026, 6, 22, 9, 0, 0, 0, time.UTC)
	got, ok := recurrenceIDInstant(ev)
	if !ok || !got.Equal(want) {
		t.Errorf("RecurrenceID = %v (ok=%v), want %v", got, ok, want)
	}
	if !ev.IsOverride {
		t.Error("IsOverride = false, want true for an instance REQUEST")
	}
}

