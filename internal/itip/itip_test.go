// recurrenceIDInstant resolves a calendar.Event's RecurrenceID (a JSCalendar
// LocalDateTime + zone) back to the UTC instant the iCalendar RECURRENCE-ID
// encoded, for comparison in tests.
func recurrenceIDInstant(ev calendar.Event) (time.Time, bool) {
	if ev.RecurrenceID == nil {
		return time.Time{}, false
	}
	return recurrenceIDToTime(ev.RecurrenceID, ev.RecurrenceIDTimeZone), true
}

// fixedDate is the deterministic clock used for every composed message so the
// generated bytes (Date header, etc.) are reproducible.
var fixedDate = time.Date(2026, 6, 19, 9, 30, 0, 0, time.UTC)

// fakeWriter is a calendar.Writer that records the CreateEvent call instead of
// touching a backend. It stamps a synthetic ID on the returned event so callers
// can tell the stored event apart from the input.
type fakeWriter struct {
	createCalls int
	gotCalendar string
	gotEvent    calendar.Event
	createErr   error
}

func (f *fakeWriter) CreateEvent(_ context.Context, calendarID string, e calendar.Event) (calendar.Event, error) {
	f.createCalls++
	f.gotCalendar = calendarID
	f.gotEvent = e
	if f.createErr != nil {
		return calendar.Event{}, f.createErr
	}
	e.ID = "stored-id"
	e.CalendarID = calendarID
	return e, nil
}

func (f *fakeWriter) UpdateEvent(_ context.Context, e calendar.Event) (calendar.Event, error) {
	return e, nil
}

func (f *fakeWriter) DeleteEvent(_ context.Context, _ string) error { return nil }

// fakeSender is an smtp.Sender that records the envelope and raw bytes of the
// Send call instead of opening a connection.
type fakeSender struct {
	sendCalls int
	gotFrom   string
	gotTo     []string
	gotRaw    []byte
	sendErr   error
}

func (f *fakeSender) Send(_ context.Context, from string, to []string, raw []byte) error {
	f.sendCalls++
	f.gotFrom = from
	f.gotTo = to
	f.gotRaw = raw
	return f.sendErr
}

func (f *fakeSender) Close() error { return nil }

// sampleRequest builds a realistic raw iMIP REQUEST through the engine itself:
// Request() produces the VCALENDAR and Compose() wraps it in the RFC 822 message,
// so the bytes are exactly the shape Parse expects.
func sampleRequest(t *testing.T) []byte {
	t.Helper()
	inv := scheduling.Invite{
		UID:       "abc-123@example.com",
		Summary:   "Project sync",
		Start:     time.Date(2026, 6, 15, 15, 0, 0, 0, time.UTC),
		End:       time.Date(2026, 6, 15, 16, 0, 0, 0, time.UTC),
		Sequence:  2,
		Organizer: scheduling.Address{Name: "Olivia Organizer", Email: "olivia@example.com"},
		Attendees: []scheduling.Attendee{
			{Address: scheduling.Address{Name: "Andy Attendee", Email: "andy@example.com"}},
		},
	}
	ics, err := scheduling.Request(inv)
	if err != nil {
		t.Fatalf("build request ics: %v", err)
	}
	raw, err := scheduling.Compose(
		inv.Organizer,
		[]scheduling.Address{inv.Attendees[0].Address},
		"Invitation: "+inv.Summary,
		scheduling.MethodRequest,
		ics,
		fixedDate,
	)
	if err != nil {
		t.Fatalf("compose request message: %v", err)
	}
	return raw
}

func TestEventFromInvite(t *testing.T) {
	inv := &scheduling.Invite{
		UID:       "uid-1@example.com",
		Summary:   "Project sync",
		Start:     time.Date(2026, 6, 15, 15, 0, 0, 0, time.UTC),
		End:       time.Date(2026, 6, 15, 16, 0, 0, 0, time.UTC),
		Organizer: scheduling.Address{Name: "Olivia Organizer", Email: "olivia@example.com"},
		Attendees: []scheduling.Attendee{
			{Address: scheduling.Address{Name: "Andy", Email: "andy@example.com"}, PartStat: scheduling.PartStatNeedsAction},
			{Address: scheduling.Address{Name: "Bea", Email: "bea@example.com"}, PartStat: scheduling.PartStatNeedsAction},
		},
	}

	got := EventFromInvite(inv)

	if got.UID != inv.UID {
		t.Errorf("UID = %q, want %q", got.UID, inv.UID)
	}
	if got.Title != inv.Summary {
		t.Errorf("Title = %q, want %q", got.Title, inv.Summary)
	}
	if !got.StartTime().Equal(inv.Start) {
		t.Errorf("StartTime = %v, want %v", got.StartTime(), inv.Start)
	}
	if !got.EndTime().Equal(inv.End) {
		t.Errorf("EndTime = %v, want %v", got.EndTime(), inv.End)
	}
	if got.Status != StatusTentative {
		t.Errorf("Status = %q, want %q", got.Status, StatusTentative)
	}
	org, ok := got.Organizer()
	if !ok {
		t.Fatal("Organizer() not found, want Olivia Organizer")
	}
	if org.Name != "Olivia Organizer" || calendar.ParticipantEmail(org) != "olivia@example.com" {
		t.Errorf("Organizer = name=%q email=%q", org.Name, calendar.ParticipantEmail(org))
	}
	atts := got.Attendees()
	if len(atts) != 2 {
		t.Fatalf("Attendees len = %d, want 2", len(atts))
	}
	wantAtt := []struct{ name, email string }{
		{"Andy", "andy@example.com"},
		{"Bea", "bea@example.com"},
	}
	for i, w := range wantAtt {
		if atts[i].Name != w.name || calendar.ParticipantEmail(atts[i]) != w.email {
			t.Errorf("Attendees[%d] = name=%q email=%q, want %q/%q",
				i, atts[i].Name, calendar.ParticipantEmail(atts[i]), w.name, w.email)
		}
		if atts[i].ParticipationStatus != "needs-action" {
			t.Errorf("Attendees[%d] partStat = %q, want needs-action", i, atts[i].ParticipationStatus)
		}
	}
}

